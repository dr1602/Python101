# Operador de modulo, que da igual al residuo

# Residuo cero por devision exacta
print(10 % 2);

# Residuo existente, por ser division inexacta
print(10 % 3);

# Operador de doble division // que nos da solo el entero resultante de una division
print(10 // 3);

# Operador de potencia
print(10 ** 3);

# Operacion mas compleja

print(2 ** 3 + 3 - 7 / 1 // 4);

# Este es el proceso de resolucion
print(2 ** 3) # 8
print(7 / 1 // 4) # 1
print( 8 + 3 - 1) # 10

